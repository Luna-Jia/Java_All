package ramdom;

public class IfElseIfGrade {
    public static void main(String[] args){
        int x = 51;
        if (x > 50)
            System.out.println("A");
        else if (x < 60)
            System.out.println("B");
        else if (x > 50 && x<60)
            System.out.println("C");
    }
}
